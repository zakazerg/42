#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str != '\0')
	{
		write(1, str, 1);
		str++;
	}
}

void main(int argc, char *argv[])    /* command line Arguments */
{
    ft_putstr(argv[0]);
    write(1, "\n", 1);
}
